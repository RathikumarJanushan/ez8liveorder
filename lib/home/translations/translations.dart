  /// Simple helper to retrieve text by key.
  /// You can optionally handle the "%{variable}" replacement inside here if desired.
  static String text(String key, {Map<String, dynamic>? params}) {
    final lang = currentLanguage.value;
    String translated = _localizedValues[lang]?[key] ??
        _localizedValues['English']?[key] ??
        key;

    // If we have params like %{quantity}, do a simple replacement:
    if (params != null) {
      params.forEach((paramKey, value) {
        translated = translated.replaceAll('%{$paramKey}', value.toString());
      });
    }
    return translated;
  }
